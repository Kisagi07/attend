import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";
import "react-toastify/ReactToastify.css";
import { ToastContainer } from "react-toastify";
import Provider from "./components/Provider";
import { auth } from "@/app/api/auth/[...nextauth]/authConfig";
import { SWR } from "./components";
import Maintenance from "./components/Maintenance";
import { cookies } from "next/headers";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Attendance",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await auth();
  const maintenance = process.env.MAINTENANCE_MODE;

  if (maintenance === "on") {
    const cookieStore = cookies();
    const key = cookieStore.get("maintenance_key") || { value: "" };
    if (key.value !== process.env.MAINTENANCE_KEY) {
      return (
        <html lang="en">
          <body className={`${inter.className} bg-white`}>
            <Maintenance />
          </body>
        </html>
      );
    }
  }

  return (
    <html lang="en">
      <body className={`${inter.className} bg-white`}>
        <Provider session={session}>
          <ToastContainer />
          <SWR>{children}</SWR>
        </Provider>
      </body>
    </html>
  );
}
